from django.contrib import admin
from .models import Author, Book

# Регистрация модели Author с помощью декоратора
@admin.register(Author)
class AuthorAdmin(admin.ModelAdmin):
    """
    Настройки административного интерфейса для модели Author.
    Определяет, как авторы будут отображаться и управляться в админке.
    """
    
    # Поля, отображаемые в списке всех авторов
    list_display = ['name', 'birth_year', 'country']
    # Будут показаны колонки: Имя | Год рождения | Страна
    
    # Поля, по которым можно выполнять поиск
    search_fields = ['name']
    # Поиск работает по имени автора (частичное совпадение)
    
    # Поля для фильтрации в правой боковой панели
    list_filter = ['country']
    # Можно фильтровать авторов по стране


# Регистрация модели Book с помощью декоратора
@admin.register(Book)
class BookAdmin(admin.ModelAdmin):
    """
    Настройки административного интерфейса для модели Book.
    Определяет, как книги будут отображаться и управляться в админке.
    """
    
    # Поля, отображаемые в списке всех книг
    list_display = ['title', 'author', 'publication_year', 'is_available']
    # Колонки: Название | Автор | Год публикации | Доступность
    
    # Поля для фильтрации в правой боковой панели
    list_filter = [
        'is_available',      # Фильтр по доступности (Да/Нет)
        'publication_year',  # Фильтр по году публикации
        'author'            # Фильтр по автору (выпадающий список)
    ]
    
    # Поля, по которым можно выполнять поиск
    search_fields = [
        'title',           # Поиск по названию книги
        'author__name',    # Поиск по имени автора (двойное подчеркивание для связей)
        'isbn'             # Поиск по ISBN
    ]
    # Поиск работает по всем указанным полям одновременно
    
    # Иерархическая навигация по датам
    date_hierarchy = 'created_at'
    # Добавляет навигацию по годам/месяцам/дням над списком книг
    # Позволяет быстро фильтровать книги по дате добавления
